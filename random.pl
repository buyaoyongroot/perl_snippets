#!/usr/local/bin/perl
#
# A CGI script that produces uncompressible random garbage directly
# from memory (no disk access).
# This is useful for throughput tests.
#
# There is one parameter, the file size in MB, 5 MB in
# this example:
#
# http://www.your.servername.here/cgi-bin/random.pl?5
#
# Written by Marc Liyanage <liyanage@access.ch>
#




$MEGABYTES = $ENV{QUERY_STRING};
$MEGABYTES = 1 unless($MEGABYTES > 0 and $MEGABYTES < 100);

$data = unpack("u", join("", <DATA>));
die "Bad data length!" unless (length($data) == 65536);

print "Content-Length: " . (1024 * 1024 * $MEGABYTES) . "\n";
print "Content-Type: application/garbage\n\n";

print $data x 16 foreach (1 .. $MEGABYTES);



